// como declarar varivaeis
// tres pissibilidades: const - constante, let - escopo local, var - escopo global

// comparação : ==
var comparação = 0 == 0;
console.log(comparação)

// comparação identica: compara os valores e os tipos
var comparacaoIdentica = 0 === "000"
console.log(comparacaoIdentica)


// Operadores aritiméticos:  + adição, - subtração, * multiplicação, / divisão, % divisão inteira, ** potenciação

// adição
var adicao = 1+1;
console.log(adicao)

// subtração
var subtracao = 1-1
console.log(subtracao)

// multiplicação 
var multiplicacao = 2 * 3
console.log(multiplicacao)

// divisao real
var divisaoReal  = 6 / 2
console.log(divisaoReal)

// divisão inteira
var divisaoInteira = 5 % 2
console.log(divisaoInteira)

// pontenciação
var pontenciacao = 2 ** 10
console.log(pontenciacao)

// Operadores relacionais: > maior que, < menor que, >= maior ou igual a, <= menor ou igual a

// maior que
var maiorQue = 5 > 2
console.log(maiorQue)

// menor que
var menorQue = 5 < 2;
console.log(menorQue)

// maior ou igual a
var maiorOuIgual = 5 >= 2
console.log(maiorOuIgual)

// menor ou igual a
var menorOuIgual = 5 <= 2
console.log(menorOuIgual)